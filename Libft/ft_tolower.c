int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

int	tolower(int c)
{
	if (ft_isalpha(c))
		return (c + 32);
	return (c);
}
